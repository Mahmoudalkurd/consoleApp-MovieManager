-- Create the 'Kunder' table
CREATE TABLE Kunder (
    id INT PRIMARY KEY,
    name VARCHAR(100),
    telephone VARCHAR(20),
    personal_id VARCHAR(20),
    address VARCHAR(255)
);

-- Create the 'Konton' table
CREATE TABLE Konton (
    id INT PRIMARY KEY,
    kunder_id INT,
    type VARCHAR(50),
    saldo DECIMAL(10, 2),
    FOREIGN KEY (kunder_id) REFERENCES Kunder(id)
);

-- Create the 'Transaktioner' table
CREATE TABLE Transaktioner (
    id INT PRIMARY KEY,
    from_konton_id INT,
    to_konton_id INT,
    amount DECIMAL(10, 2),
    date DATE,
    FOREIGN KEY (from_konton_id) REFERENCES Konton(id),
    FOREIGN KEY (to_konton_id) REFERENCES Konton(id)
);

